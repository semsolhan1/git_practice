package basic.begin;
/**
 * 문서 생성용 주석이다.
 * 배포용 문서에 설명을 달아 놓을 때 사용합니다.
 * @author ict03-24
 * @version 1.0
 * @since 2023.02.17 ~
 */
//이 파일은 주석을 연습하는 파일입니다.
public class CommentsExample {

	public static void main(String[] args) {
		/*
		#main 함수(메서드)는 프로그램 실행 시 자동으로 동작하는 함수이며,
		반드시 필요한 함수입니다.
		
		# 자바 코드를 해석하여 실행시키는 자바 가상 머신(JVM)은
		프로그램 실행 시 자동으로 main함수를 불러서 내부의 코드를
		실행시킵니다.
		*/
		
		//syso + Ctrl + space : 빠른 출력 함수를 생성.
		//괄호 안에 "는 입력을 해야 두개가 생성한다. 기호안에 내용을 써야한다.
		//Ctrl + F11키 : 실행 단축키이다.
		// ctrl + /키 : 원하는 위치에 커서를 배치 후 주석처리&해제가 가능하다.
		// ctrl + alt + 방향키 위 & 아래 : 복사가 가능하다.
		System.out.println("반갑습니다");
		System.out.println("안녕하세요");
		System.out.println("오호 이렇게 출력이 되는군요");
		
	}

}
